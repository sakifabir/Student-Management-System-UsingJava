    private void jbtnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnsearchActionPerformed
        try {
            Class.forName("org.apache.derby.jdbc.ClientDriver");
            con = DriverManager.getConnection("jdbc:derby://localhost:1527/SystemAnalysis", "Jishan", "Jishan");
            ps = con.prepareStatement("SELECT * FROM JISHAN.ADDNEWSTUDENT where ID_NUMBER=?");
            ps.setInt(1, Integer.parseInt(jtxtname.getText()));
            rs = ps.executeQuery();

            while (rs.next()) {
                jtxtfullN.setText(rs.getString("FULL_NAME"));
                jtxtdob.setText(rs.getString("DATE_OF_BIRTH"));
                jtxtFather.setText(rs.getString("FATHER_NAME"));
                jtxtMother.setText(rs.getString("MOTHER_NAME"));
                jtxtcontc.setText(rs.getString("CONTACT_NUMBER"));
                jtxybloodg.setText(rs.getString("BLOOD_GROUP"));
                Jtxtpsc.setText(rs.getString("PSC_OR_PEC"));
                JTXTJSC.setText(rs.getString("JSC"));
                jtxtssc.setText(rs.getString("SSC"));
                jtxtlol.setText(rs.getString("P_DISTRICT"));
                jtxtdistrict.setText(rs.getString("PR_DISTRICT"));
                JOptionPane.showMessageDialog(null, "Congratulations...!!! \nWe can findout your data from our database...!!! \nThank you for using student management system...!!!");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jbtnsearchActionPerformed

