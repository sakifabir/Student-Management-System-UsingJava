    private void jbtnsearch1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnsearch1ActionPerformed
        try {
            Class.forName("org.apache.derby.jdbc.ClientDriver");
            con = DriverManager.getConnection("jdbc:derby://localhost:1527/SystemAnalysis", "Jishan", "Jishan");
            ps = con.prepareStatement("SELECT * FROM JISHAN.RESULT where ID=?");
            ps.setInt(1, Integer.parseInt(jtxtid.getText()));
            rs = ps.executeQuery();

            while (rs.next()) {
                jtxtname.setText(rs.getString("NAME"));
                jtxtdept.setText(rs.getString("DEPARTMENT"));
                jtxtsem.setText(rs.getString("SEMESTER"));
                jtxtadv.setText(rs.getString("ADVISOR"));
                jtxtresult.setText(rs.getString("RESULT"));
                JOptionPane.showMessageDialog(null, "Congratulations...!!! \nWe can findout your data from our database...!!! \nThank you for using student management system...!!!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jbtnsearch1ActionPerformed

