    private void jbtnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnsaveActionPerformed
        try {
            Class.forName("org.apache.derby.jdbc.ClientDriver");
            Connection conn = DriverManager.getConnection("jdbc:derby://localhost:1527/SystemAnalysis", "Jishan", "Jishan");
            PreparedStatement ps = conn.prepareStatement("insert into Attendance values (?,?,?,?,?,?,?,?,?,?,?,?,?,?)");
            ps.setString(1, jtxtdate6564.getText());
            ps.setString(2, jtxtday.getText());
            ps.setString(3, jtxtyear.getText());
            ps.setString(4, jtxtat.getText());
            ps.setString(5, jtxtsemester.getText());
            ps.setString(6, jtxtdt.getText());
            ps.setString(7, jtxttake.getText());
            ps.setString(8, jtxtdate1.getText());
            ps.setString(9, jtxtday2.getText());
            ps.setString(10, jtxtreat1.getText());
            ps.setString(11, jtxtattim.getText());
            ps.setString(12, jtxtsemester1.getText());
            ps.setString(13, jtxtdddep.getText());
            ps.setString(14, jtxtattejhj.getText());
            ps.executeUpdate();
            conn.close();
            JOptionPane.showMessageDialog(null, "Congratulations...!!! \nYour data has been successfully store in our database...!!! \nThank you for using student management system...!!!");
            System.out.println("Congratulations...!!! Data save successfully...!!!");
            ps.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jbtnsaveActionPerformed

